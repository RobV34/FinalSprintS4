CREATE DATABASE color_DB;

CREATE TABLE Vibes (
    id INT PRIMARY KEY AUTO_INCREMENT,
    vibeName VARCHAR(255) NOT NULL
);

CREATE TABLE Styles (
    id INT PRIMARY KEY AUTO_INCREMENT,
    styleName VARCHAR(255) NOT NULL
);

CREATE TABLE Spaces (
    id INT PRIMARY KEY AUTO_INCREMENT,
    spaceName VARCHAR(255) NOT NULL,
    suggestedAddOns JSON
);

CREATE TABLE Colors (
    id INT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(255) NOT NULL,
    hexNumber VARCHAR(7) NOT NULL,
    spaceId INT,
    styleId INT,
    FOREIGN KEY (spaceId) REFERENCES Spaces(id),
    FOREIGN KEY (styleId) REFERENCES Styles(id)
);

CREATE TABLE ColorVibes (
    colorId INT,
    vibeId INT,
    FOREIGN KEY (colorId) REFERENCES Colors(id),
    FOREIGN KEY (vibeId) REFERENCES Vibes(id),
    PRIMARY KEY (colorId, vibeId)
);

CREATE TABLE Promotions (
    id INT PRIMARY KEY AUTO_INCREMENT,
    colorId INT,
    spaceId INT,
    url VARCHAR(255),
    FOREIGN KEY (colorId) REFERENCES Colors(id),
    FOREIGN KEY (spaceId) REFERENCES Spaces(id)
);

